const now = new Date()
const isoDaysAgo = (days: number, hour = 9) => {
  const value = new Date(now)
  value.setDate(value.getDate() - days)
  value.setHours(hour, 0, 0, 0)
  return value.toISOString()
}
const isoDaysAhead = (days: number, hour = 18) => {
  const value = new Date(now)
  value.setDate(value.getDate() + days)
  value.setHours(hour, 0, 0, 0)
  return value.toISOString()
}

